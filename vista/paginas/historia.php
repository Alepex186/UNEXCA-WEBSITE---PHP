        <h2 class="titulos">Historia</h2>
        <p class="parrafo">
            La Universidad Nacional Experimental de la Gran Caracas (UNEXCA) se creó en el marco de la Misión Alma Mater, como institución educativa con personalidad jurídica y patrimonio propio, distinto e independiente del Tesoro Nacional. Surgió a partir de la fusión de los Colegios Universitarios de Caracas (CUC), “Francisco de Miranda” (CUFM) y “José Lorenzo Pérez” (CUJLP). Su creación la anunció el Presidente Nicolás Maduro en los actos conmemorativos del Día del Estudiante, el 21 de noviembre de 2017, como herramienta del pueblo venezolano para contribuir con la soberanía científica tecnológica.
        </p>

        <p class="parrafo">
            La creación de la Unexca se oficializó el 27 de febrero de 2018, según Decreto Nº 3.293 publicado en la Gaceta Oficial Nro. 41.349. Asume la gestión de los Programas Nacionales de Formación (PNF), carreras y Programas Nacionales de Formación Avanzada autorizados (PNFA) a los distintos Colegios Universitarios, que tras la fusión pasaon a conformar los núcleos de la Universidad.
        </p>
